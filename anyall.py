# Use any() and all() on a list of booleans.

bools = [True, True, False, True]

# any() returns True if at least one element is True
print(any(bools))  # Output: True

# all() returns True only if all elements are True
print(all(bools))  # Output: False
#Quick Explanation:
# any(bools) → checks if any value in the list is True
#all(bools) → checks if all values are True

